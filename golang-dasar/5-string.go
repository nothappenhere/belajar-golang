package main
import "fmt"

func main(){
	fmt.Println("Muhammad")
	fmt.Println("Muhammad Rizky")
	fmt.Println("Muhammad Rizky Akbar")

	fmt.Println(len("Muhammad")) //* mengembalikan panjang karakter (8)
	fmt.Println("Muhammad Rizky"[0]) //* mengembalikan dalam bentuk byte (77)
	fmt.Println("Muhammad Rizky Akbar"[5]) //* mengembalikan dalam bentuk byte (109)
}